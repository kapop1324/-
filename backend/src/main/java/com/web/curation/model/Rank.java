package com.web.curation.model;

public class Rank {
	
	private int recipe_cnt;
	private int recipe_like_cnt;
	private int follower_cnt;
	private int achieve_cnt;
	private int recipe_comment_cnt;
	private int recipe_comment_like_cnt;
	
	public int getRecipe_cnt() {
		return recipe_cnt;
	}
	public void setRecipe_cnt(int recipe_cnt) {
		this.recipe_cnt = recipe_cnt;
	}
	public int getRecipe_like_cnt() {
		return recipe_like_cnt;
	}
	public void setRecipe_like_cnt(int recipe_like_cnt) {
		this.recipe_like_cnt = recipe_like_cnt;
	}
	public int getFollower_cnt() {
		return follower_cnt;
	}
	public void setFollower_cnt(int follower_cnt) {
		this.follower_cnt = follower_cnt;
	}
	public int getAchieve_cnt() {
		return achieve_cnt;
	}
	public void setAchieve_cnt(int achieve_cnt) {
		this.achieve_cnt = achieve_cnt;
	}
	public int getRecipe_comment_cnt() {
		return recipe_comment_cnt;
	}
	public void setRecipe_comment_cnt(int recipe_comment_cnt) {
		this.recipe_comment_cnt = recipe_comment_cnt;
	}
	public int getRecipe_comment_like_cnt() {
		return recipe_comment_like_cnt;
	}
	public void setRecipe_comment_like_cnt(int recipe_comment_like_cnt) {
		this.recipe_comment_like_cnt = recipe_comment_like_cnt;
	}
	
}