package com.web.curation.model;

public class AchieveTitleDetail {

	String id;
	String title;
	String achieve_slave_name;
	String achieve_slave;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAchieve_slave_name() {
		return achieve_slave_name;
	}
	public void setAchieve_slave_name(String achieve_slave_name) {
		this.achieve_slave_name = achieve_slave_name;
	}
	public String getAchieve_slave() {
		return achieve_slave;
	}
	public void setAchieve_slave(String achieve_slave) {
		this.achieve_slave = achieve_slave;
	}
	
	
}
